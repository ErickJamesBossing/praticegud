package practice_mga_lods;
public class Practice_Mga_Lods {
    public static void main(String[] args) {
        int x = 10;
        float a = 2.99f;
        int y = 100;
        float b = x * a;
        float c = y - b;
        char aw = '$';
        System.out.println("My balance: " + aw + y);
        System.out.println("Total items: " + aw + x);
        System.out.println("Price per items: " + aw + a);
        System.out.println("Total price of items: "+ aw + b);
        System.out.println("Left balance: "+ aw + c);
    }
}
